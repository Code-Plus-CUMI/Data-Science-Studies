#	**************************
#	** Confidence Intervals **
#	**************************
#
# 	As far as there are situations that is quite impossible to
# get datas from a whole population, we likely collect data from
# a sample from the population.
# 
#	With this, the sample statistics, like mean and median,
# are not always equal to the population stats. So, in order to check
# out if the sample stats are not too far from the population stats,
# we use the Confidence Intervals to measure it.
#